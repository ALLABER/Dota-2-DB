package com.allaber.dota2db.Item;

public class Ability {
    int imageID;
    String textAbilityName;
    String textAbilityType;
    String textAbilityLoreTop;
    String textAbilityAlt;
    String textAbilityRange;
    String textCooldown;
    String textMana;
    String textAbilityLoreBottom;

    public String getTextAbilityAlt() {
        return textAbilityAlt;
    }

    public void setTextAbilityAlt(String textAbilityAlt) {
        this.textAbilityAlt = textAbilityAlt;
    }

    public int getImageID() {
        return imageID;
    }

    public void setImageID(int imageID) {
        this.imageID = imageID;
    }

    public String getTextAbilityName() {
        return textAbilityName;
    }

    public void setTextAbilityName(String textAbilityName) {
        this.textAbilityName = textAbilityName;
    }

    public String getTextAbilityType() {
        return textAbilityType;
    }

    public void setTextAbilityType(String textAbilityType) {
        this.textAbilityType = textAbilityType;
    }

    public String getTextAbilityLoreTop() {
        return textAbilityLoreTop;
    }

    public void setTextAbilityLoreTop(String textAbilityLoreTop) {
        this.textAbilityLoreTop = textAbilityLoreTop;
    }

    public String getTextAbilityRange() {
        return textAbilityRange;
    }

    public void setTextAbilityRange(String textAbilityRange) {
        this.textAbilityRange = textAbilityRange;
    }

    public String getTextCooldown() {
        return textCooldown;
    }

    public void setTextCooldown(String textCooldown) {
        this.textCooldown = textCooldown;
    }

    public String getTextMana() {
        return textMana;
    }

    public void setTextMana(String textMana) {
        this.textMana = textMana;
    }

    public String getTextAbilityLoreBottom() {
        return textAbilityLoreBottom;
    }

    public void setTextAbilityLoreBottom(String textAbilityLoreBottom) {
        this.textAbilityLoreBottom = textAbilityLoreBottom;
    }
}
